const Groq = require("groq-sdk");

// ======================================================
// 🤖 AIクライアント初期化
// ======================================================
const client = new Groq({
  apiKey: process.env.GROQ_API_KEY,
  // 🔑 Groq API接続キー
});

// ======================================================
// 🧭 ルート定義（dispatcherと完全統一）
// ======================================================
const ROUTES = [
  "chat",      // 💬 雑談
  "post",      // ✍️ 投稿・文章生成（旧writer）
  "ocr",       // 🖼 画像・OCR解析（旧readbook）
  "receipt",   // 🧾 レシート・家計
  "schedule"   // 📅 予定管理
];

// ======================================================
// 🧠 dispatcherAI本体（意味分類エンジン）
// ======================================================
async function dispatcherAI(input = {}) {
  try {

    // ==================================================
    // 📦 入力正規化（安全化）
    // ==================================================
    const text = input.text ?? "";
    // 💬 テキスト入力（null安全）

    const ocr = input.ocr ?? "";
    // 🖼 OCR結果（画像解析結果）

    const imageHint = input.imageHint ?? "";
    // 🧩 将来拡張用（画像種類補助情報）

    // ==================================================
    // 🧪 AI入力統合（コンテキスト生成）
    // ==================================================
    const mergedInput = `
[TEXT]
${text}

[OCR]
${ocr}

[IMAGE_HINT]
${imageHint}
`.trim();

    console.log("🧠 dispatcherAI INPUT");
    console.log("--------------------------------");
    console.log(mergedInput);
    console.log("--------------------------------");
    // 👉 AIに渡る前の状態確認（重要ログ）

    // ==================================================
    // 🧾 プロンプト（分類ルール定義）
    // ==================================================
    const prompt = `
あなたはLINEメッセージ分類AIです。

ユーザー入力（テキスト＋OCR）を見て、
必ず以下の5つのどれか1つだけ返してください：

- chat     ：雑談・会話
- post     ：投稿・文章生成・執筆
- ocr      ：画像・資料・本・解析
- receipt  ：レシート・家計
- schedule ：予定・スケジュール

【厳守ルール】
- 出力は1単語のみ
- JSON禁止
- 改行禁止
- 記号禁止
- 必ず上記5つのいずれか

入力：
${mergedInput}
`;

    // ==================================================
    // 🤖 AI呼び出し
    // ==================================================
    const res = await client.chat.completions.create({
      model: "llama-3.1-70b-versatile",
      messages: [
        {
          role: "system",
          content: "You are a strict classification engine. Output only one label from fixed set."
          // 🧠 ブレ防止（固定出力モード）
        },
        {
          role: "user",
          content: prompt
        }
      ],
      temperature: 0.2,
      // 🔥 安定性優先（創造性を抑える）
    });

    // ==================================================
    // 📥 AI出力取得
    // ==================================================
    let route = res.choices?.[0]?.message?.content?.trim();

    console.log("🧠 RAW OUTPUT:", route);

    // ==================================================
    // 🧹 正規化（揺れ対策）
    // ==================================================
    if (typeof route === "string") {
      route = route.toLowerCase();
      // chat / Chat / CHAT 対応
    }

    // ==================================================
    // 🧱 バリデーション（安全制御）
    // ==================================================
    if (!ROUTES.includes(route)) {
      console.warn("⚠️ INVALID ROUTE → fallback to chat:", route);
      return "chat";
      // 🧯 不正出力は必ずchatに落とす
    }

    // ==================================================
    // ✅ 正常ルート確定
    // ==================================================
    console.log("✅ ROUTE SELECTED:", route);

    return route;

  } catch (err) {

    // ==================================================
    // 🔥 エラーフォールバック
    // ==================================================
    console.error("❌ dispatcherAI ERROR:", err);

    return "chat";
    // 🧯 システム停止防止（必ず動く設計）
  }
}

// ======================================================
// 📤 export
// ======================================================
module.exports = { dispatcherAI };
